package br.com.kardec.algoritmos;

public class Ponto3D {
    private int x, y, z;

    public Ponto3D(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
